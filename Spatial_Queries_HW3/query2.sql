SELECT CURRENT TIMESTAMP FROM SYSIBM.SYSDUMMY1;

SELECT
TF.FACILITY_NAME,
cast(DB2GSE.ST_Distance(TF.GEOLOCATION , DB2GSE.ST_POINT(-73.116371, 40.913780, 1), 'STATUTE MILE') AS DECIMAL(10, 3)) as distance
FROM
CSE532.TRIFACILITY AS TF
WHERE
TF.CARCINOGEN  = 'YES'
AND
DB2GSE.ST_Contains(DB2GSE.ST_Buffer(DB2GSE.ST_POINT(-73.116371, 40.913780, 1), 10, 'STATUTE MILE'), TF.GEOLOCATION) = 1
ORDER BY distance
LIMIT 1
;

